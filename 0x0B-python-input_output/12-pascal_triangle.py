#!/usr/bin/python3
'''this script contains function of pascal triangle'''


def pascal_triangle(n):
    '''create a lists of shape of a triangle'''
    if n <= 0:
        return []
    triangle = [[1]]
    for i in range(1, n):
        row = [1]
        for j in range(1, i):
            row.append(triangle[i - 1][j - 1] + triangle[i - 1][j])
        row.append(1)
        triangle.append(row)
    return triangle
